// Funkcia na simuláciu hodu kockou
void hod_kockou(void) {
    uint32_t start_time = HAL_GetTick(); // Získa aktuálny čas v milisekundách
    uint32_t elapsed_time = 0;
    uint8_t final_values[3];
    uint16_t display_value = 0;

    // Simulácia "hádzania" pre všetky tri displeje
    while (elapsed_time < 2000) {
        for (int i = 0; i < 3; i++) {
            uint8_t random_value = (rand() % 6) + 1; // Náhodné číslo od 1 do 6
            final_values[i] = random_value;
        }

        // Vytvorenie čísla pre zobrazenie na displeji
        display_value = (final_values[0] * 100) + (final_values[1] * 10) + final_values[2];
        sct_value(display_value, 0); // Zobrazenie čísla na displeji

        // Postupné spomaľovanie (čas medzi zmenami čísiel sa zvyšuje)
        HAL_Delay(50 + (elapsed_time / 10));

        elapsed_time = HAL_GetTick() - start_time; // Aktualizácia uplynutého času
    }

    // Generovanie konečných hodnôt kocky
    for (int i = 0; i < 3; i++) {
        final_values[i] = (rand() % 6) + 1;
    }

    // Vytvorenie konečného čísla pre zobrazenie na displeji
    display_value = (final_values[0] * 100) + (final_values[1] * 10) + final_values[2];
    sct_value(display_value, 0); // Zobrazenie konečného čísla na displeji
}


