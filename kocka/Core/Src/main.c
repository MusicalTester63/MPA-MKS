static uint16_t debounce2 = 0xFFFF;

void button_advanced(void) {
	static uint32_t off_time = 0;
	debounce2 = (debounce2 << 1) | HAL_GPIO_ReadPin(S2_GPIO_Port, S2_Pin);

	if (debounce2 == 0x7FFF) {
		HAL_GPIO_WritePin(S2_GPIO_Port, S2_Pin, GPIO_PIN_SET);
		off_time = HAL_GetTick() + 1000;
	}

	if (HAL_GetTick() > off_time) {
		HAL_GPIO_WritePin(S2_GPIO_Port, S2_Pin, GPIO_PIN_RESET);
	}
}

